// يمكن لاحقًا ربطه بأي API خارجي مثل GPT

function askAI(question) {
  return new Promise((resolve) => {
    // محاكاة استجابة AI (يمكن استبدالها بAPI حقيقي)
    setTimeout(() => {
      let answer = "AI قيد التعلم، جاري تحليل السؤال...";
      
      // أمثلة أولية للردود الذكية
      if (/من أنت/i.test(question)) {
        answer = "أنا أيهم، العقل الرقمي الحي الذي يتعلم مثل العقل البشري.";
      } else if (/نصيحة|مساعدة/i.test(question)) {
        answer = "سأحلل الموقف وأعطيك أفضل خيار متعلم من خبرات الأسئلة السابقة.";
      } else if (/سعر|عملات|فرص/i.test(question)) {
        answer = "أستطيع مراقبة الأسعار والفرص العالمية وسأعطيك التوصيات مباشرة.";
      }

      resolve(answer);
    }, 300); // استجابة سريعة
  });
}
